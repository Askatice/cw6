package com.company;

public class animal {
    private String color, breed;
    private int age;

    public animal(String color, String breed, int age) {
        this.color = color;
        this.breed = breed;
        this.age = age;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getBreed() {
        return breed;
    }

    public void setBreed(String breed) {
        this.breed = breed;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
